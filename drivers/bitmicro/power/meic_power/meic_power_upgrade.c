unsigned short meic_crc16(unsigned char * buf, unsigned char len)
{
    unsigned char
    uchCRCHi = 0xff;
    unsigned char uchCRCLo = 0xff;
    unsigned short uIndex;
    while(len--)
    {
        uIndex = uchCRCHi ^ *buf++;
        uchCRCHi = uchCRCLo ^ dgus_auchCRCHi[uIndex];
        uchCRCLo = dgus_auchCRCLo[uIndex];
    }
    return (unsigned short)(uchCRCHi << 8 | uchCRCLo);
}

unsigned char *BIN_Data = NULL;
int BIN_Data_Length;

//*************************************
//发送指令重启电源
int ResetPower(struct meic_power *power)
{
    unsigned char cmd[6] = {0xA5,0x20,0x00,0xC5};//对应APP里面的重启指令
    int Result;

    Result = meic_power_i2c_write(power->client, cmd ,4);
    return Result;
}
/*************************************/
//发送固件信息
int SendFWInfomition(struct meic_power * power)
{
    unsigned char cmd[12] = {0xA5, 0x11, 0x00, 0x06, 0x01, 0, 0, 0, 0, 0, 0, 0};//对应APP里面的重启指令
    unsigned char ReadData[8] = {0};
    unsigned short CRC;
    int Result;
    cmd[6] = (BIN_Data_Length >> 24) & 0xff;
    cmd[7] = (BIN_Data_Length >> 16) & 0xff;
    cmd[8] = (BIN_Data_Length >> 8)  & 0xff;
    cmd[9] = (BIN_Data_Length >> 0)  & 0xff;
    CRC = meic_crc16(cmd, 10);
    cmd[10] = (CRC >> 8) & 0xff;
    cmd[11] = (CRC >> 0) & 0xff;

    Result = meic_power_i2c_write(power->client, cmd, 12);
    if (Result == -1) return -1;
    msleep(500);
    Result = meic_power_i2c_read(power->client, ReadData, 1);
    if (Result == -1)
        return -1;
    if (ReadData[0] != 0xAA) {
        printk("%s: get error ret: %x\n", __func__, ReadData[0]);
        return -1;
    }
    else
        return 0;
}
/************************************/
int UpdateFW(struct meic_power *power)
{
    unsigned char cmd[128] = {0};//对应APP里面的重启指令
    unsigned char ReadData[8] = { 0 };
    unsigned short CRC;
    const int Frame_DataLen = 32;//一帧数据长度

    int frame_Len ;//一帧数据长度
    int frame_Number ;//文件总数据帧数量
    int Current_frame;//当前更新数据帧
    int i;
    int LastFrame_DataLen;//最后一帧数据长度
    int Result;
    frame_Number = BIN_Data_Length / Frame_DataLen;
    LastFrame_DataLen = BIN_Data_Length % Frame_DataLen;
    if (LastFrame_DataLen > 0) frame_Number++;
    //完整数据包
    for (Current_frame = 0; Current_frame < frame_Number; Current_frame++)
    {
        if (Current_frame != (frame_Number - 1))
        {
            frame_Len = Frame_DataLen;
        }
        else
        {   //最后一帧数据
            frame_Len = LastFrame_DataLen;
        }

        /***********************/
        cmd[0] = 0xA5;
        cmd[1] = 0x12;
        cmd[2] = 0x0;
        cmd[3] = frame_Len;
        for (i = 0; i < frame_Len; i++)
        {
            cmd[4 + i] = BIN_Data[Current_frame*frame_Len+i];
        }
        CRC = meic_crc16(cmd, frame_Len+4);
        cmd[frame_Len+4] = (CRC >> 8) & 0xff;
        cmd[frame_Len+5] = (CRC >> 0) & 0xff;
        printk("%s: %d/%d %d%%\n", __func__, Current_frame, frame_Number-1,
                Current_frame*100 / (frame_Number-1));
        Result = meic_power_i2c_write(power->client, cmd, frame_Len + 6);
        msleep(250);//延时时间根据数据帧长度来定,此段时间内，正在擦除写入Flash
        if (Result == -1) return -1;
        Result = meic_power_i2c_read(power->client, ReadData, 1);
        if (Result < 0 || ReadData[0] != 0xAA)
        {
            printk("%s: get error ret: %d, %x\n", __func__, Result, ReadData[0]);
            return -1;
        }
    }

    return 0;
}
/*******************************/
void UpdateFinished(struct meic_power *power)
{
    unsigned char cmd[8] = { 0xA5, 0x13 ,0x00, 0x01, 0, 0, 0 };
    unsigned short CRC;
    CRC = meic_crc16(cmd, 5);
    cmd[5] = (CRC >> 8) & 0xff;
    cmd[6] = (CRC >> 0) & 0xff;

   meic_power_i2c_write(power->client, cmd, 7);//完成指令不需要检测返回状态，因为此时APP可能已经启动
}

int FWupdate_Task(struct meic_power *power)
{
    int ret = 0;

    ret = ResetPower(power);
//    if (ret < 0) {
//        printk("%s: reset power failed !\n", __func__);
//        return -1;
//    }

    msleep(50);
    ret = SendFWInfomition(power);
    if (ret < 0) {
        printk("%s: send fw info failed !\n", __func__);
        return -1;
    }

    ret = UpdateFW(power);
    if (ret < 0) {
        printk("%s: upgrade power failed !\n", __func__);
        return -1;
    }

    msleep(500);
    printk("%s: download firmware done !\n", __func__);
    UpdateFinished(power);
    printk("%s: upgrade firmware success !\n", __func__);
    return 0;
}

int meic_power_load_firmware(const char *file)
{
    int ret, len;
    struct kstat stat;

    struct file *fp = filp_open(file, O_RDONLY, 0600);
    if (IS_ERR(fp)) {
        pr_err("%s: failed to open file %s\n", __func__, file);
        return PTR_ERR(fp);
    }

    ret = vfs_getattr(&fp->f_path, &stat);
    if (ret < 0) {
        pr_err("%s: failed to get file %s stat\n", __func__, file);
        return ret;
    }

    BIN_Data_Length = stat.size;
    printk("%s: get file %s, size: %lld\n", __func__, file, stat.size);

    BIN_Data = kmalloc(stat.size, GFP_KERNEL);
    if (BIN_Data == NULL) {
        pr_err("%s: malloc error\n", __func__);
        return -1;
    }

    len = kernel_read(fp, 0, BIN_Data, BIN_Data_Length);
    if (len != BIN_Data_Length) {
        pr_err("%s: read file %s error, len: %d\n", __func__, file, len);
        return -1;
    }

    printk("%s: load firmware %s success !\n", __func__, file);
    return 0;
}

int meic_power_upgrade(struct meic_power *power, const char *file)
{
    power->is_upgrading = 1;
    meic_power_load_firmware(file);
    FWupdate_Task(power);
    power->is_upgrading = 0;
    return 0;
}

